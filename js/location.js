function getWeatherReport(city) {
  fetch(
    `${weatherApi.main_url}?q=${city}&appid=${weatherApi.api_key}&units=metric`,
  )
    .then((weather) => {
      return weather.json();
    })
    .then(showWeatherReport);
}

