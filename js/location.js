function dateCreate(dateParams) {
  const days = [
    'Sunday',
    'Monday',
    'Tuesday',
    'Wednesday',
    'Thursday',
    'Friday',
    'Saturday',
  ];

  const months = [
    'January',
    'February',
    'March',
    'April',
    'May',
    'June',
    'July',
    'August',
    'September',
    'October',
    'November',
    'December',
  ];

  let year = dateParams.getFullYear();
  let month = months[dateParams.getMonth()];
  let date = dateParams.getDate();
  let day = days[dateParams.getDay()];

  return `${day}, ${date} ${month} ${year}`;
}
